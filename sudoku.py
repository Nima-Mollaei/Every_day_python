# این برنامه یک سودوکو 9 در 9 را حل میکند




def is_valid(board, row, col, num):
    # بررسی سطر
    for x in range(9):
        if board[row][x] == num:
            return False

    # بررسی ستون
    for x in range(9):
        if board[x][col] == num:
            return False

    # بررسی بلوک 3x3
    start_row, start_col = 3 * (row // 3), 3 * (col // 3)
    for i in range(3):
        for j in range(3):
            if board[i + start_row][j + start_col] == num:
                return False

    return True

def solve_sudoku(board):
    for row in range(9):
        for col in range(9):
            if board[row][col] == 0:
                for num in range(1, 10):
                    if is_valid(board, row, col, num):
                        board[row][col] = num
                        if solve_sudoku(board):
                            return True
                        board[row][col] = 0
                return False
    return True

def sudoku(puzzle):
    solve_sudoku(puzzle)
    return puzzle

